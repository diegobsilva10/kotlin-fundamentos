    println("A soma dos números é ${n1 + n2} ")

    //Subtração
    var numeroSubtracao1 = 50
    var numeroSubtracao2 = 5
    println("A substração dos números é: ${numeroSubtracao1 - numeroSubtracao2}")

    //Multiplicação
    var numeroMultiplicacao1 = 2
    var numeroMultiplicacao2 = 2
    println("A multiplicação dos números é: ${numeroMultiplicacao1 * numeroMultiplicacao2}")

    //Divisão
    var numeroDivisao1 = 2
    var numeroDivisao2 = 2
    println("A divisão dos números é: ${numeroDivisao1 / numeroDivisao2 }")

